    private void calc() {

        if (txt_buyingprice.getText().equals("") || txt_qty.getText().equals("") || txt_discount.getText().equals("")) {

        } else {
            String qty = txt_qty.getText();
            String buyingPrice = txt_buyingprice.getText();
            int qtyI = Integer.parseInt(qty);
            double buyingPriceD = Double.parseDouble(buyingPrice);
            String discount = txt_discount.getText();
            int discountI = Integer.parseInt(discount);
            double tot;
            if (Radio_persontage.isSelected()) {

                tot = (qtyI * buyingPriceD) / 100 * discountI;
                tot = buyingPriceD * qtyI - tot;
                String totS = Double.toString(tot);
                txt_totalprice.setText(totS);
            } else {
                tot = qtyI * buyingPriceD - discountI;
                String totS = Double.toString(tot);
                txt_totalprice.setText(totS);
            }
        }
    }

