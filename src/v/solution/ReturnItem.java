private void InvoiceTable() {
        ResultSet rs1;
        dtm = (DefaultTableModel) table_invoice.getModel();
        dtm.setRowCount(0);
        try {

            String query = "SELECT * FROM sale WHERE status = '1'";
            rs1 = DB.search(query);

            while (rs1.next()) {
                Vector v = new Vector();
                String id = rs1.getString("Id");
                String date = rs1.getString("date");
                String billAmount = rs1.getString("FinalTotal");

                v.add(id);
                v.add(date);
                v.add(billAmount);

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void filtertablesubCat(String query) {
        dtm = (DefaultTableModel) table_invoice.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dtm);
        table_invoice.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(query));
    }

    private void clear() {
        txt_refundAmount.setText("");

        txt_invID.setText("");

        txt_qty.setText("");

        Radio_status.doClick();

        dtm = (DefaultTableModel) tble_product.getModel();
        dtm.setRowCount(0);
        txt_soldItemId.setText("");
        txt_oneItemPrice.setText("");

    }

    private void calc() {
        int qty = Integer.parseInt(txt_qty.getText());
        double itemPrice = Double.parseDouble(txt_oneItemPrice.getText());
        double refundPrice = itemPrice * qty;
        String rP = String.valueOf(refundPrice);

        txt_refundAmount.setText(rP);
    }

