    private void btn_returnItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_returnItemActionPerformed
        // insert data to purchase:
        int option = JOptionPane.showConfirmDialog(this, "SURE ?");

        if (option == 0) {
            if (NumberValidation.validateNumber(txt_qty.getText())) {

                if (CurrencyValidation.validateCurrency(txt_refundAmount.getText())) {

                    double refundAmount = Double.parseDouble(txt_refundAmount.getText());
                    String saleId = txt_invID.getText();
                    int quantity = Integer.parseInt(txt_qty.getText());
                    int reselStatus = 1;

                    long lastInsertId = 0;
                    if (Radio_statusNo.isSelected()) {
                        reselStatus = 0;
                    } else {
                        reselStatus = 1;
                    }

                    try {
                        String query = "INSERT INTO `return`(`RefundAmount`, `sale_Id`) VALUES ('" + refundAmount + "','" + saleId + "')";
                        DB.push(query);

                        query = "select Id from `return` where Id=(SELECT LAST_INSERT_ID())";

                        ResultSet rs = DB.search(query);

                        if (rs.next()) {
                            lastInsertId = rs.getLong("Id");

                        }

                        query = "INSERT INTO `return_item`(`Resell_Status`, `Quantity`, `ReturnPrice`, `return_Id`, `sold_item_Id`) VALUES ('" + reselStatus + "','" + quantity + "', '" + refundAmount + "','" + lastInsertId + "', '" + saleId + "')";
                        DB.push(query);

                        query = "UPDATE `available_quantity` SET `Quantity`= `Quantity`+'" + quantity + "' WHERE `item_Id`='" + txt_soldItemId.getText() + "'";
                        DB.push(query);
                        JOptionPane.showMessageDialog(this, "Item Is Returned");
                        clear();
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Check Refund Amount");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Check Quantity");
            }
        }

    }//GEN-LAST:event_btn_returnItemActionPerformed

    private void table_invoiceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_invoiceMouseClicked
        // TODO add your handling code here:
        String pId;

        int selectedRow = table_invoice.getSelectedRow();

        /////////////
        if (selectedRow != -1) {

            dtm = (DefaultTableModel) table_invoice.getModel();
            pId = dtm.getValueAt(selectedRow, 0).toString();
            txt_invID.setText(pId);
            ResultSet rs1;
            dtm = (DefaultTableModel) tble_product.getModel();
            dtm.setRowCount(0);
            try {

                String query = "SELECT * FROM sold_item WHERE sale_Id = '" + selectedRow + "'";
                rs1 = DB.search(query);

                while (rs1.next()) {
                    Vector v = new Vector();
                    String id = rs1.getString("item_Id");
                    String discount = rs1.getString("DiscountFroItem");
                    String sellingPrice = rs1.getString("RetailPrice");
                    String qty = rs1.getString("Quantity");
                    String total = rs1.getString("Total");
                    String soldId = rs1.getString("sale_Id");
                    double refund = Double.parseDouble(sellingPrice) - Double.parseDouble(discount) / Double.parseDouble(qty);

                    v.add(id);
                    v.add(discount);
                    v.add(sellingPrice);
                    v.add(qty);
                    v.add(total);
                    v.add(soldId);
                    v.add(refund);

                    dtm.addRow(v);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_table_invoiceMouseClicked

