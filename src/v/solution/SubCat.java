    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        String query = "SELECT * FROM subcat WHERE Id = '" + lbl_Id.getText() + "' AND status = '1'";
        int alreadyStatus = 0;
        try {
            rs = DB.search(query);
            if (rs.next()) {
                alreadyStatus = 1;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (alreadyStatus == 0) {
            if (txt_subcatname.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Missiong Details");
            } else {

                String query2 = "INSERT INTO `subcat`(Name) VALUES ("
                        + "'" + txt_subcatname.getText() + "')";
                try {
                    DB.push(query2);
                    JOptionPane.showMessageDialog(this, "successfully saved");
                    categoryTable();
                    clear();
                } catch (Exception ex) {
                    Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "This Category is Already Exists");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

