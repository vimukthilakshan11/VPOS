    private void categoryTable() {
       
        dtm = (DefaultTableModel) table_subcat.getModel();
        dtm.setRowCount(0);
        try {

            String query = "SELECT * FROM subcat WHERE status = '1'";
            rs = DB.search(query);

            while (rs.next()) {
                Vector v = new Vector();
                String catName = rs.getString("Name");
                String id = rs.getString("Id");
                
                
                 v.add(id);
                v.add(catName);
               

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clear() {
        txt_subcatname.setText("");
      
        txt_search.setText("");
    }

    private void filtertableCat(String query) {
        dtm = (DefaultTableModel) table_subcat.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dtm);
        table_subcat.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(query));
    }
