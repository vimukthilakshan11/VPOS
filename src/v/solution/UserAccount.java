    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:

        String empId = txt_empId.getText();
        String query3 = "SELECT * FROM userprofile WHERE employee_Id = '" + empId + "' AND status = '1'";
        int alreadyStatus = 0;
        try {
            rs = DB.search(query3);
            if (rs.next()) {
                alreadyStatus = 1;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (alreadyStatus == 0) {

            String userName = txt_username.getText();
            String password = txt_password.getText();
            String repassword = txt_reEnterPassword.getText();

            if (!password.equals(repassword)) {
                JOptionPane.showMessageDialog(this, "Passwords Are Not Same");
            } else {
                if (txt_empId.getText().equals("") || txt_password.getText().equals("")
                        || txt_reEnterPassword.equals("") || txt_username.getText().equals("")) {

                    JOptionPane.showMessageDialog(this, "Missiong Details");
                } else {
                    String encrpp = Encryption.getMd5(password);
                    
                    String query = "INSERT INTO `userprofile`(`UserName`, `Password`,`employee_Id`) VALUES ('" + userName + "','" + encrpp + "','" + empId + "')";

                    try {
                        DB.push(query);
                        JOptionPane.showMessageDialog(this, "successfully saved");
                        clear();

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "This Employee is Already Has A Account");
        }


    }//GEN-LAST:event_btn_saveActionPerformed

